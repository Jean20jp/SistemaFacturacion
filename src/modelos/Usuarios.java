
package modelos;

public class Usuarios {
    
    String cedula;
    String nombre;
    String apellido;
    String tipUser;

    public Usuarios(String cedula, String nombre, String apellido, String tipUser) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.apellido = apellido;
        this.tipUser = tipUser;
    }
}
